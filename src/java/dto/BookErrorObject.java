package dto;

/**
 *
 * @author mevrthisbang
 */
public class BookErrorObject {
    private String titleError, authorError, issuersError, publishingCompanyError, sizeError, coverTypeError, releasedDate,
            numOfPageError, priceError, descriptionError, shortDescriptionError, thumnailImgError, mainImgError;

    public BookErrorObject() {
    }

    public String getTitleError() {
        return titleError;
    }

    public void setTitleError(String titleError) {
        this.titleError = titleError;
    }

    public String getAuthorError() {
        return authorError;
    }

    public void setAuthorError(String authorError) {
        this.authorError = authorError;
    }

    public String getIssuersError() {
        return issuersError;
    }

    public void setIssuersError(String issuersError) {
        this.issuersError = issuersError;
    }

    public String getPublishingCompanyError() {
        return publishingCompanyError;
    }

    public void setPublishingCompanyError(String publishingCompanyError) {
        this.publishingCompanyError = publishingCompanyError;
    }

    public String getSizeError() {
        return sizeError;
    }

    public void setSizeError(String sizeError) {
        this.sizeError = sizeError;
    }

    public String getCoverTypeError() {
        return coverTypeError;
    }

    public void setCoverTypeError(String coverTypeError) {
        this.coverTypeError = coverTypeError;
    }

    public String getReleasedDate() {
        return releasedDate;
    }

    public void setReleasedDate(String releasedDate) {
        this.releasedDate = releasedDate;
    }

    public String getNumOfPageError() {
        return numOfPageError;
    }

    public void setNumOfPageError(String numOfPageError) {
        this.numOfPageError = numOfPageError;
    }

    public String getPriceError() {
        return priceError;
    }

    public void setPriceError(String priceError) {
        this.priceError = priceError;
    }

    public String getDescriptionError() {
        return descriptionError;
    }

    public void setDescriptionError(String descriptionError) {
        this.descriptionError = descriptionError;
    }

    public String getShortDescriptionError() {
        return shortDescriptionError;
    }

    public void setShortDescriptionError(String shortDescriptionError) {
        this.shortDescriptionError = shortDescriptionError;
    }

    public String getThumnailImgError() {
        return thumnailImgError;
    }

    public void setThumnailImgError(String thumnailImgError) {
        this.thumnailImgError = thumnailImgError;
    }

    public String getMainImgError() {
        return mainImgError;
    }

    public void setMainImgError(String mainImgError) {
        this.mainImgError = mainImgError;
    }
    
}
